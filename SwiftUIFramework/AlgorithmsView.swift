import Foundation

 public class Alorithms{
   
     public static func addtingNumbers(a:Int, b:Int)-> Int{
       return a + b
     }
     
     public static func substractingNumbers(a:Int, b:Int)-> Int{
       return a - b
     }
     
     public static func multiplyingNumbers(a:Int, b:Int)-> Int{
       return a * b
     }
     
     public static func dividingNumbers(a:Int, b:Int)-> Int{
       return a % b
     }
     
     public static func averageNumbers(a:Int, b:Int)-> Int{
       return a / b
     }
     
}
